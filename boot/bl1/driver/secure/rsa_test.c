void revert32(char *dst, char *src, int size)
{
    int i;
    for (i = 0; i < size; i += 4) {
        dst[i] = src[i + 3];
        dst[i + 1] = src[i + 2];
        dst[i + 2] = src[i + 1];
        dst[i + 3] = src[i + 0];
    }
}
void *valInternal_ReverseMemCpy(
    void *Dest_p,
    const void *Src_p,
    int Size)
{
    char *dp = Dest_p;
    const char *sp = Src_p;

    sp += (Size - 1);
    while (Size--) {
        *dp++ = *sp--;
    }
    return Dest_p;
}
void mem_copy(char *dst, char *src, int size)
{
    int i;
    for (i = 0; i < size; i++) {
        dst[i] = src[i];
    }
}
